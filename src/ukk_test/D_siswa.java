    private void tablesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablesMouseClicked
        String nisn, nis, nama, idKelas, alamat, telp, idSpp;
        int list = tables.rowAtPoint(evt.getPoint());
        
        nisn = tables.getValueAt(list, 0).toString();
        txt_nisn.setText(nisn);
        
        nis = tables.getValueAt(list, 1).toString();
        txt_nis.setText(nis);
        
        nama = tables.getValueAt(list, 2).toString();
        txt_nama.setText(nama);
        
        idKelas = tables.getValueAt(list, 3).toString();
        txt_idKelas.setText(idKelas);
        
        alamat = tables.getValueAt(list, 4).toString();
        txt_alamat.setText(alamat);
        
        telp = tables.getValueAt(list, 5).toString();;
        txt_telp.setText(telp);
        
        idSpp = tables.getValueAt(list, 6).toString();
        txt_idSpp.setText(idSpp);
    }//GEN-LAST:event_tablesMouseClicked

