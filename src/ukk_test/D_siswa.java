    private void btn_buatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buatActionPerformed
        String sql ,nisn, nis, nama, idKelas, alamat, telp, idSpp;
        
        nisn = txt_nisn.getText();
        nis = txt_nis.getText();
        nama = txt_nama.getText();
        idKelas = txt_idKelas.getText();
        alamat = txt_alamat.getText();
        telp = txt_telp.getText();
        idSpp = txt_idSpp.getText();
        
        if (nisn.equals("") || nisn.length() == 0){
            JOptionPane.showMessageDialog(this, "Masukan nisn");
        } else if (nisn.length() > 11 ){
            JOptionPane.showMessageDialog(this, "Maksimal karakter nisn adalah 11");
        } else if (nis.equals("") ||  nis.length() == 0){
            JOptionPane.showMessageDialog(this, "Masukan nis");
        } else if (nis.length() > 8){
            JOptionPane.showMessageDialog(this, "Maksimal karakter nis adalah 8");
        } else if (nama.equals("")){
            JOptionPane.showMessageDialog(this, "Masukan nama");
        } else if (idKelas.equals("") ||  idKelas.length() == 0){
            JOptionPane.showMessageDialog(this, "Masukan id Kelas");
        } else if (idKelas.length() > 11){
            JOptionPane.showMessageDialog(this, "Maksimal karakter id kelas adalah 11");
        } else if (alamat.equals("")){
            JOptionPane.showMessageDialog(this, "Masukan alamat");
        } else if (telp.equals("") ||  telp.length() == 0){
            JOptionPane.showMessageDialog(this, "Masukan no telpon");
        } else if (telp.length() > 13){
            JOptionPane.showMessageDialog(this, "Maksimal karakter no telpon adalah 13");
        } else if (idSpp.equals("") ||  idSpp.length() == 0){
            JOptionPane.showMessageDialog(this, "Masukan id Spp");
        } else if (idSpp.length() > 11){
            JOptionPane.showMessageDialog(this, "Maksimal karakter id spp adalah 11");
        }
        
        try{ 
            sql = "INSERT into 'siswa' ('nisn', 'nis', 'nama', 'id_kelas', 'alamat', 'no_telp', 'id_spp') values ('"+nisn+"', '"+nis+"', '"+nama+"', '"+idKelas+"', '"+alamat+"', '"+telp+"', '"+idSpp+"') ";
            Connection conn = (Connection)Config.config();
            java.sql.PreparedStatement pst = conn.prepareCall(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "data berhasil ditambahkan");
            
            loads();
            hapused();
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btn_buatActionPerformed

